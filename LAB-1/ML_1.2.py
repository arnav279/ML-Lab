import numpy as np

# Experiment 2: 3x3 Zero Matrix and 2x5 Ones Matrix
print("3x3 Zero Matrix:")
zeros_matrix = np.zeros((3, 3))
print(zeros_matrix)
print(f"Shape: {zeros_matrix.shape}\n")

print("2x5 Ones Matrix:")
ones_matrix = np.ones((2, 5))
print(ones_matrix)
print(f"Shape: {ones_matrix.shape}")